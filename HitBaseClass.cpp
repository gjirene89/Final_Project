# include "HitBaseClass.h"

//==============================================================================
//  関数名       CHit_Base
//  説明			当たり判定ベースクラスのコンストラクタ
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          なし
//==============================================================================
CHit_Base::CHit_Base(void)
{
	m_type = HIT_NONE;
	m_entity = nullptr;
	m_isVisible = true;
	m_color = COLOR_NORMAL;
	m_isHit = false;

	return;
}

//==============================================================================
//  関数名       CHit_Base
//  説明			当たり判定ベースクラスのコンストラクタ
//------------------------------------------------------------------------------
//	引数
//          GAMEHIT_TYPE	type	ゲームオブジェクトの種類
//	戻り値
//          なし
//==============================================================================
CHit_Base::CHit_Base(GAMEHIT_TYPE type)
{
	m_type = type;
	m_entity = nullptr;
	m_isVisible = true;
	m_color = COLOR_NORMAL;
	m_isHit = false;

	return;
}

//==============================================================================
//  関数名       ~CHit_Base
//  説明			当たり判定ベースクラスのデストラクタ
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          なし
//==============================================================================
CHit_Base::~CHit_Base(void)
{
	m_entity = nullptr;

	return;
}

//==============================================================================
//  関数名       Initialize
//  説明			当たり判定のオブジェクトの色を強調する
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          なし
//==============================================================================
void CHit_Base::Initialize(void)
{
	m_isHit = false;
	m_color = COLOR_NORMAL;

	return;
}

//==============================================================================
//  関数名       UpdatePosition
//  説明			位置を更新する
//------------------------------------------------------------------------------
//	引数
//          XMFLOAT3	position		新しい位置情報
//	戻り値
//          なし
//==============================================================================
void CHit_Base::UpdatePosition(XMFLOAT3 position)
{
	m_position = position;

	return;
}

//==============================================================================
//  関数名        Hightlight
//  説明			当たり判定のオブジェクトの色を強調する
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          なし
//==============================================================================
void CHit_Base::Highlight()
{
	m_color = COLOR_HLIGHT;

	return;
}

//==============================================================================
//  関数名       GetHitType
//  説明			当たり判定の種類を戻す
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//			GAMEHIT_TYPE	当たり判定の種類
//==============================================================================
GAMEHIT_TYPE CHit_Base::GetHitType(void)
{
	return m_type;
}

//==============================================================================
//  関数名       GetPosition
//  説明			位置情報を戻す
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          XMFLOAT3	 位置情報
//==============================================================================
XMFLOAT3 CHit_Base::GetPosition(void)
{
	return m_position;
}

//==============================================================================
//  関数名        GetEntity
//  説明          連結しているゲームオブジェクトを戻す
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          CGameObjectBase* 　　ゲームオブジェクトへのポインタ
//==============================================================================
CGameObjectBase* CHit_Base::GetEntity(void)
{
	return m_entity;
}

