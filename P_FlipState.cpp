//==============================================================================
//!	@fn		Enter
//!	@brief	開始関数
//!	@param	CPlayer *player		プレイヤーのポインタ
//!	@retval	なし
//!	@note	
//==============================================================================
void FlipState::Enter(CSackBase* player)
{
	//紐を回す関数
	((CPlayer*)player)->FlipString();
}

//==============================================================================
//!	@fn		Action
//!	@brief	処理関数
//!	@param	CPlayer *player		プレイヤーのポインタ
//!	@retval	なし
//!	@note	
//==============================================================================
void FlipState::Action(CSackBase* player)
{
	//今は空
}

//==============================================================================
//!	@fn		PostAction
//!	@brief	後処理関数
//!	@param	CPlayer *player		プレイヤーのポインタ
//!	@retval	なし
//!	@note	
//==============================================================================
void FlipState::PostAction(CSackBase* player)
{
	//normalStateに戻す
	player->SetState(SACK_NORMAL);
}


