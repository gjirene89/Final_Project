//==============================================================================
//  関数名		 Initialize	
//  説明			 CFpsの初期関数
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          なし
//==============================================================================
void CFps::Initialize()
{
	//変数の初期化
	m_fps = 0;
	m_count = 0;
	m_startTime = timeGetTime();
	
	return;
}

//==============================================================================
//  関数名		 Frame	
//  説明			 CFpsの毎フレーム更新関数
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          なし
//==============================================================================
void CFps::Frame()
{
	m_count++;

	if (timeGetTime() >= (m_startTime + 1000))
	{
		m_fps = m_count;
		m_count = 0;

		m_startTime = timeGetTime();
	}
	
}

//==============================================================================
//  関数名		 GetFps	
//  説明			 秒ごとのフレーム数を戻す関数
//------------------------------------------------------------------------------
//	引数
//          なし
//	戻り値
//          int      m_fps			秒ごとのフレーム数
//==============================================================================
int CFps::GetFps()
{
	return m_fps;
}

